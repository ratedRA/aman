package com;

public class SourceOrika {
    private String name;
    private String age;
    private String replicate;

    public SourceOrika(String name, String age, String replicate) {
        this.name = name;
        this.age = age;
        this.replicate = replicate;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getReplicate() {
        return replicate;
    }

    public void setReplicate(String replicate) {
        this.replicate = replicate;
    }
}
